      <h3>Database management systems,known as DBMSs, are software programs that act as a connecting point between databases and the various users and applications that need to access them. The purpose of a DBMS is to provide businesses with an access point to create, retrieve, modify and organize their vast amounts of data.Though the terms have become somewhat synonymous over time, a database and a DBMS are not the same thing. A database is simply a collection of related data. A DBMS, on the other hand, is a tool to manage and organize multiple databases. In other words, the database stores the data, while the DBMS accesses and manipulates it.</h3>
    
